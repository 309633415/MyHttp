package demoinfo.spring.aop;

public class Audience {  
	public void takeSeat()  {  
		System.out.println("观众坐在座位上。");  
	}  
	public void turnOffPhone()   {  
		System.out.println("观众关闭手机。");  
	}  
	public void applaud()  {  
		System.out.println("观众鼓掌，掌声经久不息...");  
	}  
	public void unHappy()  {  
		System.out.println("观众不高兴.");  
	}  
}  
